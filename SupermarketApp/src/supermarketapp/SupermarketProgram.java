    private String getIDFromList(JList ProductList){
        String[] ListRowParts;
        String ListRow;
        if((ListRow =(String) ProductList.getSelectedValue())==(null)){
            ProgramStatus.setText("No Product Found");
            return null;
        }
        ListRowParts=(ListRow.split("-"));
        return ListRowParts[1];
    }
    
