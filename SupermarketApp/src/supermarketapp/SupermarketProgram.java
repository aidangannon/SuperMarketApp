    private void ProductChooseImageFileButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProductChooseImageFileButtonActionPerformed
        int returnValue = ProductImageFileChooser.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            String path = ProductImageFileChooser.getSelectedFile().getPath();
                if((new ImageIcon(path).getIconWidth()<=89)&&(new ImageIcon(path).getIconHeight()<=89)){
                    ProductImageFileTextBox.setText(path);
                    ProgramStatus.setText("Image was selected");
                }else{
                    ProgramStatus.setText("Image can't be larger than 89x89");
                }
        }
    }//GEN-LAST:event_ProductChooseImageFileButtonActionPerformed

    private void ProductNamesValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ProductNamesValueChanged
        String ID=getIDFromList(ProductNames);
        if(ID!=null){
            Product SelectedProduct = Products.Search(ID);
            SelectedProduct.DisplayImage(ImageIcon);
            if(SelectedProduct.getOutOfStock()){
                if(CurrentUser.IsOwner()){
                    ProgramStatus.setText("Out Of Stock NEED TO RE-ORDER");
                }else{
                    ProgramStatus.setText("Out Of Stock");
                }
            }else{ProgramStatus.setText("In Stock");}
        }
    }//GEN-LAST:event_ProductNamesValueChanged

